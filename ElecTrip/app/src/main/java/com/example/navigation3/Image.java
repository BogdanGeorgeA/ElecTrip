package com.example.navigation3;

public class Image {
    private String id;
    private String url;
    private Integer width;
    private Integer height;
    private String type;

    public String getId() {
        return id;
    }

    public String getUrl() {
        return url;
    }

    public Integer getWidth() {
        return width;
    }

    public Integer getHeight() {
        return height;
    }

    public String getType() {
        return type;
    }
}