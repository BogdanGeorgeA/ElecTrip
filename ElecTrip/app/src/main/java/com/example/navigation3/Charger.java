package com.example.navigation3;

public class Charger {
    Standard standard;
    Float power;
    Status status;
    StationSpeedType speed;
    int total;

    public Standard getStandard() {
        return standard;
    }

    public Float getPower() {
        return power;
    }

    public Status getStatus() {
        return status;
    }

    public StationSpeedType getSpeed() {
        return speed;
    }

    public int getTotal() {
        return total;
    }
}
